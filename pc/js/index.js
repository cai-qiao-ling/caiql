$(window).scroll(function() {
    var scollH = $(window).scrollTop();
    console.log(scollH);
    if (scollH == 0) {
        $('#wrap1').css("opacity", "0");
        $('#wrap2').css("display", "none");

    } else {
        $('#wrap1').css("opacity", "1");
        $('#wrap2').css("display", "flex");
    }
})